import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const PROJECT_ROOT = join(import.meta.dir, '..', '..')
const TS_LIB_DIR = join(PROJECT_ROOT, 'node_modules', 'typescript', 'lib')
const BUN_LOCK = join(PROJECT_ROOT, 'bun.lock')
const OUT_FILE = join(
  PROJECT_ROOT,
  'src',
  'infrastructure',
  'automations',
  'typescript-validator',
  'embedded-ts-lib-types.generated.ts'
)
const REL_ROOT = '../../../..'

const resolveTypescriptVersion = (): string => {
  const lockBody = readFileSync(BUN_LOCK, 'utf8')
  const match = lockBody.match(/"typescript":\s*\[\s*"typescript@([0-9]+\.[0-9]+\.[0-9]+)"/)
  if (match === null) {
    console.error('✗ Could not find resolved typescript version in bun.lock')
    process.exit(1)
  }
  return match[1]!
}

const TS_VERSION = resolveTypescriptVersion()

interface ImportLine {
  readonly varName: string
  readonly importPath: string
}

const libFiles = readdirSync(TS_LIB_DIR)
  .filter((f) => f.startsWith('lib.') && f.endsWith('.d.ts'))
  .toSorted()

if (libFiles.length === 0) {
  console.error(`✗ No lib.*.d.ts files found under ${TS_LIB_DIR}`)
  process.exit(1)
}

const imports: readonly ImportLine[] = libFiles.map((f, idx) => ({
  varName: `_l${idx}`,
  importPath: `${REL_ROOT}/node_modules/typescript/lib/${f}`,
}))

const entries = libFiles.map((f, idx) => `  '${f}': _l${idx},`).join('\n')

const header = `/**
 * Copyright (c) 2025-2026 ESSENTIAL SERVICES
 *
 * This source code is licensed under the Business Source License 1.1
 * found in the LICENSE.md file in the root directory of this source tree.
 */

/* eslint-disable */
// @ts-nocheck
// AUTO-GENERATED by scripts/build/generate-embedded-ts-lib-types.ts — DO NOT EDIT.
// SOVRIUM_TS_LIB_VERSION: ${TS_VERSION}
// Each \`with { type: 'file' }\` import embeds the matching lib.*.d.ts into the
// compiled binary's virtual filesystem. The runtime validator (layer.ts) reads
// the file via Bun.file() — works identically in dev (real path) and in the
// compiled binary (\`/$bunfs/...\` path).
//
// The SOVRIUM_TS_LIB_VERSION sentinel is the canonical fingerprint of which
// TypeScript stdlib was embedded; \`scripts/check-embedded-ts-version-drift.ts\`
// compares it against the resolved \`typescript\` version in \`bun.lock\` and
// fails CI if they diverge — guards against a forgotten regeneration after
// \`bun add typescript@...\`.
`

const importBlock = imports
  .map((i) => `import ${i.varName} from '${i.importPath}' with { type: 'file' }`)
  .join('\n')

const body = `
/** TypeScript standard-library .d.ts basename → embedded path. */
export const TS_LIB_FILES = {
${entries}
}
`

writeFileSync(OUT_FILE, `${header}\n${importBlock}\n${body}`)
console.log(
  `✓ embedded-ts-lib-types.generated.ts — ${libFiles.length} lib.*.d.ts files (typescript@${TS_VERSION})`
)
